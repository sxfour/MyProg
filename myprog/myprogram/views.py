# http request
def index(request):
    data = {
        'title': 'Главная страница',
        'menu': menu,
        'posts': data_db,
        'cat_selected': 0,
    }
    return render(request, 'program/index.html', context=data)


def about(request):
    return render(request, 'program/about.html', {'title': 'О Сайте', 'menu': menu})


def show_post(request, post_id):
    post = get_object_or_404(Program, pk=post_id)

    data = {
        'title': post.title,
        'menu': menu,
        'post': post,
        'cat_selected': 1.
    }

    return render(request, 'program/post.html', data)

    return HttpResponse(f'Отображение статьи с id = {post_id}')


